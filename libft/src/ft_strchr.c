#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	if (*s == (char)c)
		return (s);
	while (*s)
	{
		if (*s == (char)c)
			return (s);
		s++;
	}
	if (*s == (char)c)
		return (s);
	return (NULL);
}
